/** 텍스트 미리보기 API에 자주 쓰이는 확장자(휴리스틱). */
const PREVIEW_EXTENSIONS = new Set([
  "md",
  "txt",
  "rst",
  "py",
  "ts",
  "tsx",
  "js",
  "jsx",
  "mjs",
  "cjs",
  "json",
  "yml",
  "yaml",
  "sql",
  "css",
  "scss",
  "less",
  "html",
  "htm",
  "xml",
  "svg",
  "csv",
  "log",
  "sh",
  "bash",
  "zsh",
  "bat",
  "ps1",
  "env",
  "ini",
  "toml",
  "properties",
  "gradle",
  "java",
  "kt",
  "kts",
  "go",
  "rs",
  "c",
  "h",
  "cpp",
  "hpp",
  "cs",
  "rb",
  "php",
  "swift",
  "vue",
  "svelte",
]);

export function isLikelyTextPreviewableExtension(extension: string | null | undefined): boolean {
  if (!extension) return false;
  const ext = extension.replace(/^\./, "").toLowerCase();
  return PREVIEW_EXTENSIONS.has(ext);
}
